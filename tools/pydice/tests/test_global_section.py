def test_raw_length():
    assert len(RAW_360) == 360


def test_global_params_serdes():
    params = deserialize(RAW_360)

    assert params.owner == 0xffc1000100000000
    assert params.latest_notification == 0x00000010
    assert params.nickname == "DesktopKonnekt6"
    assert params.clock_config == ClockConfig(rate=ClockRate.R48000, src=ClockSource.Internal)
    assert params.enable is False
    assert params.clock_status == ClockStatus(src_is_locked=True, rate=ClockRate.R48000)
    assert params.external_source_states == ExternalSourceStates(
        sources=[ClockSource.Arx1, ClockSource.Arx2],
        locked=[False, False],
        slipped=[False, False],
    )
    assert params.current_rate == 48000
    assert params.version == 0x01000400
    assert ClockRate.R44100 in params.avail_rates
    assert ClockRate.R48000 in params.avail_rates
    assert ClockRate.R88200 in params.avail_rates
    assert ClockRate.R96000 in params.avail_rates
    assert ClockRate.R176400 in params.avail_rates
    assert ClockRate.R192000 in params.avail_rates
    assert params.avail_sources == [ClockSource.Internal]
    assert (ClockSource.Arx1, "Stream-1") in params.clock_source_labels
    assert (ClockSource.Arx2, "Stream-2") in params.clock_source_labels
    assert (ClockSource.Internal, "INTERNAL") in params.clock_source_labels

    # Round-trip serialize (first 100 bytes must match, requires extended for version field)
    r = serialize_extended(params)
    assert r[:100] == RAW_360[:100]
